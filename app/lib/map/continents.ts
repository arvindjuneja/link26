// Helper to draw a continent path
export function drawContinent(
  ctx: CanvasRenderingContext2D,
  points: number[][],
  worldToCanvas: (lat: number, lon: number) => { x: number; y: number },
  fill = true
) {
  if (points.length < 2) return;
  
  ctx.beginPath();
  const first = worldToCanvas(points[0][0], points[0][1]);
  ctx.moveTo(first.x, first.y);
  
  for (let i = 1; i < points.length; i++) {
    const p = worldToCanvas(points[i][0], points[i][1]);
    ctx.lineTo(p.x, p.y);
  }
  
  ctx.closePath();
  
  if (fill) {
    ctx.fill();
  }
  ctx.stroke();
}

